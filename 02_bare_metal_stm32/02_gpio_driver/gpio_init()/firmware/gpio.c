#include "gpio.h"

/*-----------------------------------------------------------
    GPIO Initialization
-----------------------------------------------------------*/
void gpio_init(GPIO_TypeDef *GPIOx,
               uint8_t pin,
               uint8_t mode)
{
    /*-------------------------------------------------------
        Step 1 : Enable GPIO Clock
    -------------------------------------------------------*/

    if (GPIOx == GPIOA)
    {
        RCC->AHB1ENR |= (1U << 0);
    }
    else if (GPIOx == GPIOB)
    {
        RCC->AHB1ENR |= (1U << 1);
    }
    else if (GPIOx == GPIOC)
    {
        RCC->AHB1ENR |= (1U << 2);
    }

    /*-------------------------------------------------------
        Step 2 : Clear Existing Mode Bits
    -------------------------------------------------------*/

    GPIOx->MODER &= ~(3U << (pin * 2));

    /*-------------------------------------------------------
        Step 3 : Set New Mode
    -------------------------------------------------------*/

    GPIOx->MODER |= (mode << (pin * 2));
}
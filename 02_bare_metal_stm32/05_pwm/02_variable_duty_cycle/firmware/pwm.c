#include "pwm.h"
#include "stm32f446xx.h"

/*-------------------------------------------------
 * Initialize TIM2 Channel 1 for PWM Output on PA5
 *------------------------------------------------*/
void pwm_init(void)
{
    /*-------------------------------------------------
     * 1. Enable Clocks
     *------------------------------------------------*/
    RCC->AHB1ENR |= (1U << RCC_AHB1ENR_GPIOAEN_Pos);
    RCC->APB1ENR |= (1U << RCC_APB1ENR_TIM2EN_Pos);

    /*-------------------------------------------------
     * 2. Configure PA5 as Alternate Function
     * MODER[11:10] = 10
     *------------------------------------------------*/
    GPIOA->MODER &= ~(3U << (5 * 2));
    GPIOA->MODER |=  (2U << (5 * 2));

    /*-------------------------------------------------
     * 3. Select AF1 (TIM2_CH1)
     * AFRL[23:20] = 0001
     *------------------------------------------------*/
    GPIOA->AFRL &= ~(0xFU << (5 * 4));
    GPIOA->AFRL |=  (1U   << (5 * 4));

    /*-------------------------------------------------
     * 4. Configure Timer Frequency
     *
     * Timer Clock = 16 MHz
     * PSC = 15
     * Timer Tick = 1 MHz
     *
     * ARR = 999
     * PWM Frequency = 1 kHz
     *------------------------------------------------*/
    TIM2->PSC = 15;
    TIM2->ARR = 999;

    /*-------------------------------------------------
     * 5. Configure Channel 1 as Output
     *------------------------------------------------*/
    TIM2->CCMR1 &= ~(3U << TIM_CCMR1_CC1S_Pos);

    /*-------------------------------------------------
     * 6. Enable CCR1 Preload
     *------------------------------------------------*/
    TIM2->CCMR1 |= (1U << TIM_CCMR1_OC1PE_Pos);

    /*-------------------------------------------------
     * 7. PWM Mode 1
     * OC1M = 110
     *------------------------------------------------*/
    TIM2->CCMR1 &= ~(7U << TIM_CCMR1_OC1M_Pos);
    TIM2->CCMR1 |=  (6U << TIM_CCMR1_OC1M_Pos);

    /*-------------------------------------------------
     * 8. Initial Duty Cycle (50%)
     *------------------------------------------------*/
    TIM2->CCR1 = 500;

    /*-------------------------------------------------
     * 9. Enable Channel 1 Output
     *------------------------------------------------*/
    TIM2->CCER |= (1U << TIM_CCER_CC1E_Pos);

    /*-------------------------------------------------
     * 10. Generate Update Event
     *------------------------------------------------*/
    TIM2->EGR = 1;

    /*-------------------------------------------------
     * 11. Start Timer
     *------------------------------------------------*/
    TIM2->CR1 |= (1U << TIM_CR1_CEN_Pos);
}

/*-------------------------------------------------
 * Set PWM Duty Cycle
 *
 * duty = 0 to 100
 *------------------------------------------------*/
void pwm_set_duty(uint8_t duty)
{
    if (duty > 100)
    {
        duty = 100;
    }

    TIM2->CCR1 = ((TIM2->ARR + 1) * duty) / 100;
}